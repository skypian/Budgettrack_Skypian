    $conn = getDB();
    $query = "SELECT d.*, 
              (SELECT COUNT(*) FROM users WHERE department_id = d.id) as user_count
              FROM departments d
              ORDER BY d.dept_name";
    $stmt = $conn->prepare($query);
    $stmt->execute();
