if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    if (isset($_POST['action'])) {
        switch ($_POST['action']) {
            case 'create_department':
                $dept_name = $_POST['dept_name'] ?? '';
                $dept_code = $_POST['dept_code'] ?? '';
                $description = $_POST['description'] ?? '';
                
                if ($dept_name && $dept_code) {
                    try {
                        $conn = getDB();
                        $query = "INSERT INTO departments (dept_name, dept_code, dept_description) VALUES (:dept_name, :dept_code, :description)";
                        $stmt = $conn->prepare($query);
                        $stmt->bindParam(':dept_name', $dept_name);
                        $stmt->bindParam(':dept_code', $dept_code);
                        $stmt->bindParam(':description', $description);
                        
                        if ($stmt->execute()) {
                            $success_message = 'Department created successfully!';
                        } else {
                            $error_message = 'Failed to create department.';
                        }
                    } catch (Exception $e) {
                        $error_message = 'Error: ' . $e->getMessage();
                    }
                } else {
                    $error_message = 'Please fill in all required fields.';
                }
                break;
        }
    }
}

